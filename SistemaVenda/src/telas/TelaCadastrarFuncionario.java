    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String Nome = CampoNome.getText();
        String RG = CampoRG.getText();
        String CPF = CampoCPF.getText();
        String Tel = CampoTel.getText();
     
        
        if (this.funcionario != null) {
            
            // atualizar o produto existente
            this.funcionario.setNome(Nome);
            this.funcionario.setCpf(CPF);
            this.funcionario.setRg(RG);
            this.funcionario.setTel(Tel);
           
          
                    
            RepositorioFuncionarios.editarFuncionario(this.funcionario);
            JOptionPane.showMessageDialog(this, "Atualizado com Sucesso!" );
            
        } else {
            
            // criando um objeto Produto
            Funcionario FuncionarioAtual = new Funcionario(Nome, RG, Tel, Tel);


            // metodo opcional para visualizar dados        
            FuncionarioAtual.imprimirDados(); 

            JOptionPane.showMessageDialog(this, FuncionarioAtual);

            // salvar o objeto na lista de REPOSITORIO DE DADOS
      RepositorioFuncionarios.adicionarFuncionario(FuncionarioAtual);
        }





this.dispose();

            
    }//GEN-LAST:event_jButton1ActionPerformed

